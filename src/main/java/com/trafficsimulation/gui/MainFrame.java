        setSize(1024, 768); // Устанавливаем начальный размер окна (ширина, высота)
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE); // Указываем, что при закрытии окна приложение должно завершиться
        setLocationRelativeTo(null); // Располагаем окно по центру экрана при запуске
        // setResizable(false); // Если нужно запретить изменение размера окна - раскомментируйте

        // 2. Добавление компонентов (пока пусто, добавим позже)
        // Сюда будем добавлять панели для отрисовки, кнопки управления и т.д.

        System.out.println("Конструктор MainFrame выполнен, окно настроено.");
